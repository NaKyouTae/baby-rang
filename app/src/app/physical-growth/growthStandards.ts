/**
 * LMS 파라미터 조회 (백분위 정밀 계산용)
 */
export function getLMS(
  gender: Gender,
  metric: MetricType,
  ageMonths: number,
): GrowthEntry | null {
  const rows = lmsData[gender][metric];
  if (ageMonths < 0 || ageMonths >= rows.length) return null;
  const r = rows[ageMonths];
  return { L: r[0], M: r[1], S: r[2], p3: r[3], p15: r[4], p50: r[5], p85: r[6], p97: r[7] };
}

/**
 * 만나이 월령 계산 (이용지침서 공식 기반)
 * 소수점 버림 처리
 */
export function calcAgeMonths(birthDate: string, measureDate: string): number {
  const [by, bm, bd] = birthDate.split('-').map(Number);
  const [my, mm, md] = measureDate.split('-').map(Number);
  const months = (my - by) * 12 + (mm - bm) + (md - bd) / 30.4;
  return Math.floor(months);
}

/**
 * 표준정규분포 CDF 근사 (Abramowitz & Stegun)
 */
function normalCDF(z: number): number {
  if (z < -12) return 0;
  if (z > 12) return 1;

  const a1 = 0.254829592;
  const a2 = -0.284496736;
  const a3 = 1.421413741;
  const a4 = -1.453152027;
  const a5 = 1.061405429;
  const p = 0.3275911;

  const sign = z < 0 ? -1 : 1;
  const x = Math.abs(z) / Math.SQRT2;
  const t = 1.0 / (1.0 + p * x);
  const y = 1.0 - ((((a5 * t + a4) * t + a3) * t + a2) * t + a1) * t * Math.exp(-x * x);

  return 0.5 * (1.0 + sign * y);
}

/**
 * 측정값의 정확한 백분위수 계산 (LMS 공식)
 *
 * 이용지침서 공식:
 *   L != 0: Z = ((x/M)^L - 1) / (L*S)
 *   L == 0: Z = ln(x/M) / S
 *
 * Z값을 표준정규분포 CDF로 변환하여 백분위수 반환 (0-100)
 */
export function calcPercentile(
  gender: Gender,
  metric: MetricType,
  ageMonths: number,
  value: number,
): number | null {
  const entry = getLMS(gender, metric, ageMonths);
  if (!entry) return null;

  const { L, M, S } = entry;

  let z: number;
  if (Math.abs(L) < 0.0001) {
    // L == 0
    z = Math.log(value / M) / S;
  } else {
    // L != 0
    z = (Math.pow(value / M, L) - 1) / (L * S);
  }

  const percentile = normalCDF(z) * 100;
  return Math.round(percentile * 10) / 10;
}

/**
 * 측정값의 대략적인 백분위수 추정 (선형보간, 하위 호환용)
 */
export function estimatePercentile(
  gender: Gender,
  metric: MetricType,
  ageMonths: number,
  value: number,
): number | null {
  return calcPercentile(gender, metric, ageMonths, value);
}
